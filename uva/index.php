                    <p>
                        <b>O que é o ENADE?</b><br/><br/>
                        O Exame Nacional de Desempenho dos Estudantes (ENADE) é um dos procedimentos de avaliação do Sistema Nacional de Avaliação da Educação Superior (SINAES), criado pela <a href="http://www.planalto.gov.br/ccivil_03/_ato2004-2006/2004/lei/l10.861.htm" target="_blank">Lei nº. 10.861, de 14 de abril de 2004.</a>
                    </p>
                    <p>
                        <b>Qual o objetivo do ENADE?</b><br/><br/>
                        O objetivo do ENADE é avaliar e acompanhar o processo de aprendizagem e o desempenho acadêmico dos estudantes em relação aos conteúdos programáticos previstos nas diretrizes curriculares do respectivo curso de graduação; suas habilidades para ajustamento às exigências decorrentes da evolução do conhecimento e competências para compreender temas exteriores ao âmbito específico da profissão escolhida, ligados à realidade brasileira e mundial e a outras áreas do conhecimento. 
                    </p>

                    <p>
                        <b>O ENADE é obrigatório?</b><br/><br/>
                        Conforme disposição do art. 5º, § 5º, da Lei nº. 10.861/2004, o ENADE constitui-se componente curricular obrigatório, sendo inscrita no histórico escolar do estudante somente a situação regular com relação a essa obrigação. O estudante selecionado que não comparecer ao Exame estará em situação irregular junto ao ENADE e não poderá colar grau e consequentemente, a IES não poderá emitir seu diploma.
                    </p>

                    <p>
                        <b>Como são definidas as áreas que serão avaliadas no ENADE?</b><br/><br/>
                        O Ministério da Educação define, anualmente, as áreas propostas pela Comissão de Avaliação da Educação Superior (Conaes), órgão colegiado de coordenação e supervisão do Sinaes. A periodicidade máxima de aplicação do ENADE em cada área será trienal.
                    </p>

                    <p>
                        <b>Quais áreas serão avaliadas no ENADE 2016?</b><br/><br/>
                        Conforme a <a href="http://download.inep.gov.br/educacao_superior/enade/legislacao/2016/portaria_normativa_n05_de_09032016.pdf">Portaria Normativa nº 05 de 09 de março de 2016</a>, serão avaliados pelo Enade 2016 os cursos que:<br><br>

                        I - que conferem diploma de bacharel nas áreas de: a Agronomia; Biomedicina; Educação Física; Enfermagem; Farmácia; Fisioterapia; Fonoaudiologia; Medicina; Medicina Veterinária; Nutrição; Odontologia; Serviço Social; Zootecnia.<br><br>

                        II - que conferem diploma de tecnólogo nas áreas de: Agronegócio; Estética e Cosmética; Gestão Ambiental; Gestão Hospitalar; Radiologia
                    </p>

                    <p>
                        <b>Quando será realizado o ENADE 2016?</b><br/><br/>
                        O ENADE 2016 será aplicado no dia 20 de novembro de 2016, com início às 13h (treze horas) do horário oficial de Brasília (DF), e terá a duração de 4 (quatro) horas. Não será permitida a entrada no local da prova após esse horário. Os estudantes que participarão da prova deverão apresentar-se no local definido pelo Inep, para localizar a sala, assinar a lista de presença e cumprir outros requisitos, munidos de documento oficial de identificação (com fotografia), e caneta esferográfica de tinta preta, feita em material transparente para responder tanto as questões objetivas quanto as discursivas. A participação no ENADE 2016 será atestada a partir da assinatura do estudante na lista de presença de sala e no cartão de respostas às questões objetivas da prova.<br> 
